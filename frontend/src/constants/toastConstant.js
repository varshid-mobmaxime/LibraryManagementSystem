import { toast, Bounce } from "react-toastify";
export const ToastSuccess = (SuccessTxt = "") =>
  toast.success(SuccessTxt, {
    position: "top-right",
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: false,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
    transition: Bounce,
  });

